a=79

print("Decimal --> Binary: "+ bin(a))
print("Binary to decimal: "+ str(int(bin(a), 2)))

print(0b1000) #8 -- binary to decimal
print(0o1000) #512 -- octal to decimal
print(0x1000) #4096 -- hexadecimal to decimal


print("Decimal --> Octal: "+ oct(a))
print("Octal to decimal: "+ str(int(oct(a), 8)))

print("Decimal --> Hexadecimal: "+ hex(a))
print("Hexadecimal to decimal: "+ str(int(hex(a), 16)))




